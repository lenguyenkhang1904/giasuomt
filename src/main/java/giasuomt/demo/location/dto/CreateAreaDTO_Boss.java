package giasuomt.demo.location.dto;

public class CreateAreaDTO_Boss {
	protected String nation;
	
	protected String state;
	
	protected String provincialLevel;
	
	protected String district;
	
	protected String commune;
	
	protected String xRelCoo;	
	
	protected String yRelCoo;

	
//getter,setter
	public String getNation() {
		return nation;
	}

	public void setNation(String nation) {
		this.nation = nation;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getProvincialLevel() {
		return provincialLevel;
	}

	public void setProvincialLevel(String provincialLevel) {
		this.provincialLevel = provincialLevel;
	}

	public String getDistrict() {
		return district;
	}

	public void setDistrict(String district) {
		this.district = district;
	}

	public String getCommune() {
		return commune;
	}

	public void setCommune(String commune) {
		this.commune = commune;
	}

	public String getxRelCoo() {
		return xRelCoo;
	}

	public void setxRelCoo(String xRelCoo) {
		this.xRelCoo = xRelCoo;
	}

	public String getyRelCoo() {
		return yRelCoo;
	}

	public void setyRelCoo(String yRelCoo) {
		this.yRelCoo = yRelCoo;
	}
}
